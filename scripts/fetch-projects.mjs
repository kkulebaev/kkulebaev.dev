#!/usr/bin/env node
// Fetches GitHub metadata for repos listed in src/content/projects.whitelist.mjs
// and writes the merged result to src/content/projects.generated.ts.
// Run via `pnpm projects:fetch`. Set GITHUB_TOKEN env var to lift the
// unauthenticated 60 req/h rate limit.

import { writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'
import { projectWhitelist } from '../src/content/projects.whitelist.mjs'

const here = dirname(fileURLToPath(import.meta.url))
const outPath = resolve(here, '../src/content/projects.generated.ts')

const headers = {
  Accept: 'application/vnd.github+json',
  'User-Agent': 'kkulebaev.dev-fetch',
}
if (process.env.GITHUB_TOKEN) {
  headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`
}

async function fetchRepo(slug) {
  const res = await fetch(`https://api.github.com/repos/${slug}`, { headers })
  if (!res.ok) {
    throw new Error(`GitHub ${res.status} for ${slug}: ${await res.text()}`)
  }
  return res.json()
}

const projects = []
for (const entry of projectWhitelist) {
  const repo = await fetchRepo(entry.slug)
  const project = {
    slug: entry.slug,
    title: repo.name,
    description: repo.description ?? '',
    codeHref: repo.html_url,
  }
  if (entry.tags) project.tags = entry.tags
  if (entry.demoUrl) project.demoHref = entry.demoUrl
  projects.push(project)
}

const body = JSON.stringify(projects, null, 2)
const out = `// Auto-generated by scripts/fetch-projects.mjs. Do not edit by hand.
// Run \`pnpm projects:fetch\` to refresh.

import type { Project } from './portfolio'

export const projects: readonly Project[] = ${body}
`

await writeFile(outPath, out, 'utf8')
console.log(`Wrote ${projects.length} project${projects.length === 1 ? '' : 's'} to ${outPath}`)
